int	ft_nbrrev(int nbr)
{
	int len;
	int ret;

	ret = nbr;
	len = 1;
	ret = 0;
	while (len < nbr)
	{
		ret = ret * 10 + (nbr / len) % 10;
		len *= 10;
	}
	return (ret);
}
