#include "ft_printf.h"

int	ft_nblen(intmax_t nbr)
{
	int pow;

	pow = 0;
	if (nbr == 0)
		return (1);
	while (nbr)
	{
		pow += 1;
		nbr /= 10;
	}
	return (pow);
}
